#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (j < n && *s2)
	{
		s1[i] = *s2;
		s2++;
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
