#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while (i < len)
	{
		dst[i] = *src;
		if (*src)
			src++;
		i++;
	}
	return (dst);
}
